package com.bean;

public class Uuser {
	
	private int id;
	private String nickname;
	private String phone;
	private String type;
	private String passwd;
	private String tou_url;
	private String background;
	private String attentioners;
	private String fans;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public String getTou_url() {
		return tou_url;
	}
	public void setTou_url(String tou_url) {
		this.tou_url = tou_url;
	}
	public String getBackground() {
		return background;
	}
	public void setBackground(String background) {
		this.background = background;
	}
	public String getAttentioners() {
		return attentioners;
	}
	public void setAttentioners(String attentioners) {
		this.attentioners = attentioners;
	}
	public String getFans() {
		return fans;
	}
	public void setFans(String fans) {
		this.fans = fans;
	}
	

}
